package mypack;

public class Book {
private int bno;
private String bname;
private double bprice;
public int getBno() {
	return bno;
}
public void setBno(int bno) {
	this.bno = bno;
}
public String getBname() {
	return bname;
}
public void setBname(String bname) {
	this.bname = bname;
}
public double getPrice() {
	return bprice;
}
public void setPrice(double bprice) {
	this.bprice = bprice;
}}
